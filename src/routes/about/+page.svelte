	<h1>About This Site</h1>

	<section>
		<h2>Purpose</h2>
		<p>
			Find a translation of <i>The Iliad</i> or <i>The Odyssey</i> you'll enjoy reading. Instead of relying
			only on reviews, this tool lets you experience different styles directly. Compare short, anonymous
			passages, indicate your preference, and get guidance towards a version that fits you.
		</p>
	</section>

	<section>
		<h2>How It Works</h2>
		<ul class="list-disc space-y-1 pl-5">
			<li>
				Select a work on the <a href="/" class="text-blue-600 hover:underline">homepage</a> and click
				"Start Comparing".
			</li>
			<li>
				You'll see the <strong>same short passage</strong> in two different translations (A and B),
				selected randomly and shown <strong>anonymously</strong>.
			</li>
			<li>
				Read both versions and vote for the style you prefer using the buttons ("A is better", "B is
				better", or "Tie").
			</li>
			<li>Once you vote, the translator names for that pair are revealed.</li>
			<li>Use "New Round" to continue comparing different random passages and translators.</li>
			<li>
				The "View Results" page tracks your votes for each book, helping you see if clear
				preferences for certain translators emerge over multiple comparisons.
			</li>
		</ul>
	</section>
	<section>
		<h2>Your Data & Privacy</h2>

		<h3 class="mt-4 mb-1 text-base font-semibold">What happens to my votes?</h3>
		<p>
			All votes you cast while comparing translation passages are saved <strong
				>only on your computer</strong
			>, within your web browser's local storage. This data is not sent to any central server, and
			it remains private to you on your current device and browser.
		</p>
		<p class="mt-1">
			You can view and delete your voting history for each book on its respective "View Results"
			page.
		</p>

		<h3 class="mt-4 mb-1 text-base font-semibold">Are cookies used?</h3>
		<p>No, this website does not use cookies for tracking or any other purpose.</p>

		<h3 class="mt-4 mb-1 text-base font-semibold">What about future global results?</h3>
		<p>
			We have plans to potentially implement a feature showing aggregated, anonymous global results
			and analysis from all users. This feature is not yet available.
		</p>
		<p class="mt-1">
			<strong
				>Importantly, any votes you cast now (which are stored locally) will NOT be automatically
				included in any future global data set.</strong
			> If and when a global results feature is introduced, a new system for collecting that data (likely
			requiring your explicit consent) would be implemented separately.
		</p>
	</section>
